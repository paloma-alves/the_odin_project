def add(a,b)
  return a + b
end

def subtracts(a,b)
  return a - b
end

def multiply(a,b)
  return a * b
end

def divide(a,b)
  return a / b
end

def remainder(a,b)
  return a % b
end

def float_division(a,b)
  return a.to_f / b.to_f
end

